    var firm_name       = $("#firm_name").val();
    var firm_pan        = $("#firm_pan").val();
    var firm_gst        = $("#firm_gst").val();
    var firm_start_date = $("#firm_start_date").val();
    var city            = $("#city").val();
    var firm_type       = $('input[name="firm_type"]:checked').val();
    var major_category  = $('input[name="major_category"]:checked').val();
    var photo           = $("#projectimage")[0].files[0];

    var form_data  = new FormData();
    form_data.append('mobile_no', mobile_no);
    form_data.append('firm_name', firm_name);
    form_data.append('firm_pan', firm_pan);
    form_data.append('firm_gst', firm_gst);
    form_data.append('firm_start_date', firm_start_date);
    form_data.append('city', city);
    form_data.append('firm_type', firm_type);
    form_data.append('major_category', major_category);
    form_data.append('photo', photo);
    form_data.append('_token', '{{ csrf_token() }}');


    if(city == ''){
        Swal.fire({
            icon: "error",
            title: "Oops...",
            text: "Please select your city!",
            footer: ''
        });
        return false;
    }

    if(firm_start_date == ''){
        Swal.fire({
            icon: "error",
            title: "Oops...",
            text: "Please enter your Start Date!",
            footer: ''
        });
        return false;
    }

    if(firm_gst == ''){
        Swal.fire({
            icon: "error",
            title: "Oops...",
            text: "Please enter your Firm GST!",
            footer: ''
        });
        return false;
    }

    if(firm_pan == ''){
        Swal.fire({
            icon: "error",
            title: "Oops...",
            text: "Please enter your Firm Pan!",
            footer: ''
        });
        return false;
    }
    if(firm_name == ''){
        Swal.fire({
            icon: "error",
            title: "Oops...",
            text: "Please enter your Firm Name!",
            footer: ''
        });
        return false;
    }
    if(mobile_no == ''){
        Swal.fire({
            icon: "error",
            title: "Oops...",
            text: "Please enter your mobile no!",
            footer: ''
        });
        return false;
    }

    // if((!$('#radio').is(':checked'))){
